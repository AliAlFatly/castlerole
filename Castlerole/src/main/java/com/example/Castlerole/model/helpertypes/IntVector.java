package com.example.Castlerole.model.helpertypes;

import java.util.ArrayList;

public class IntVector {
    private int first;
    private int second;

    public IntVector(int first, int second) {
        this.first = first;
        this.second = second;
    }

    public int getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }


    public void setFirst(int first) {
        this.first = first;
    }

    public void setSecond(int second) {
        this.second = second;
    }
}
